    private void buconsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buconsultaActionPerformed
       String dado = input_consulta.getText();
       resultado = CadastroProduto.consulta(dado);
       if (resultado == null || CadastroProduto.lista_produto == null){
            JOptionPane.showMessageDialog(null,"O produto não esta cadastrado. ","aviso",JOptionPane.ERROR_MESSAGE);
       }
       else{
            DefaultTableModel tbprodutos = (DefaultTableModel) jvprodutos.getModel();
            Object[] dados = {resultado.obterCodigo(), resultado.obterNome(),resultado.obterQuantidade(),resultado.obterPreco()};
            tbprodutos.addRow(dados);
       }
    }//GEN-LAST:event_buconsultaActionPerformed

