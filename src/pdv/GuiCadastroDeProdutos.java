    private void buexcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buexcluirActionPerformed
        if(jvprodutos.getSelectedRow() != -1){
            DefaultTableModel tbprodutos = (DefaultTableModel) jvprodutos.getModel();
            int selecionado = jvprodutos.getSelectedRow();// indice selecionado pelo mouse
            tbprodutos.removeRow(selecionado);//remove a linha na tela 
            CadastroProduto.lista_produto.remove(selecionado); //remove o objeto no array
            
        }else{
            JOptionPane.showMessageDialog(null,"Selecione um produto. ","aviso",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_buexcluirActionPerformed

    private void buatualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buatualizarActionPerformed
        if(jvprodutos.getSelectedRow() != -1){
            DefaultTableModel tbprodutos = (DefaultTableModel) jvprodutos.getModel();
            int selecionado = jvprodutos.getSelectedRow();
            Produto produto = CadastroProduto.lista_produto.get(selecionado);//objeto 
            JanelaAtualizar janela = new JanelaAtualizar(produto);
            janela.setVisible(true);
            janela.setResizable(false);
            janela.setLocationRelativeTo(null);
            
        }else{
            JOptionPane.showMessageDialog(null,"Selecione um produto. ","aviso",JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_buatualizarActionPerformed

